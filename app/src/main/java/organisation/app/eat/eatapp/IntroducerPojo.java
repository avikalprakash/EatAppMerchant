package organisation.app.eat.eatapp;

public class IntroducerPojo {
  String introducer_name;
  String introducer_phone;
  String user_name;
  String redeem_branch;
  String date;
  String qr_image;

    public String getIntroducer_name() {
        return introducer_name;
    }

    public void setIntroducer_name(String introducer_name) {
        this.introducer_name = introducer_name;
    }

    public String getIntroducer_phone() {
        return introducer_phone;
    }

    public void setIntroducer_phone(String introducer_phone) {
        this.introducer_phone = introducer_phone;
    }

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getRedeem_branch() {
        return redeem_branch;
    }

    public void setRedeem_branch(String redeem_branch) {
        this.redeem_branch = redeem_branch;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getQr_image() {
        return qr_image;
    }

    public void setQr_image(String qr_image) {
        this.qr_image = qr_image;
    }
}
